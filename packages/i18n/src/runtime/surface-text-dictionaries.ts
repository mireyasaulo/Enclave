const surfaceDictionaries: Partial<Record<I18nAppSurface, LocaleDictionary>> = {
  "cloud-console": cloudConsoleText,
};

export function getSurfaceTextDictionary(
  surface: I18nAppSurface,
  locale: SupportedLocale,
) {
  const dictionary = new Map(
    Object.entries(surfaceDictionaries[surface]?.[locale] ?? {}),
  );
  if (surface === "cloud-console" && locale === "zh-CN") {
    Object.entries(cloudConsoleChineseTextOverrides).forEach(([key, value]) => {
      dictionary.set(key, value);
    });
  }
  return dictionary;
}
