export function getStageCount(): number {
  return STAGES.length;
}

function rowsAt(stageIdx: number): string[] {
  const i = ((stageIdx - 1) % STAGES.length + STAGES.length) % STAGES.length;
  return STAGES[i] as string[];
}

function charToTile(c: string): TileKind {
  switch (c) {
    case "#":
      return TILE_BRICK;
    case "=":
      return TILE_STEEL;
    case "T":
      return TILE_TREE;
    case "I":
      return TILE_ICE;
    case "W":
      return TILE_WATER;
    case "X":
      return TILE_BASE;
    default:
      return TILE_EMPTY;
  }
}

/**
 * Decode stage to 26x26 half-tile grid (8x8 px per cell).
 * Each character is one whole brick → expanded to 2x2 half-bricks.
 * Validates row/col counts at runtime (development sanity check).
 */
export function decodeStage(stageIdx: number): Uint8Array {
  const rows = rowsAt(stageIdx);
  if (rows.length !== STAGE_ROWS) {
    throw new Error(
      `tank-war stage ${stageIdx}: expected ${STAGE_ROWS} rows, got ${rows.length}`,
    );
  }
  const grid = new Uint8Array(HALF_W * HALF_H);
  for (let r = 0; r < STAGE_ROWS; r++) {
    const row = rows[r] ?? "";
    if (row.length !== STAGE_COLS) {
      throw new Error(
        `tank-war stage ${stageIdx} row ${r}: expected ${STAGE_COLS} cols, got ${row.length}`,
      );
    }
    for (let c = 0; c < STAGE_COLS; c++) {
      const tile = charToTile(row.charAt(c));
      if (tile === TILE_EMPTY) continue;
      const hr = r * 2;
      const hc = c * 2;
      grid[hr * HALF_W + hc] = tile;
      grid[hr * HALF_W + hc + 1] = tile;
      grid[(hr + 1) * HALF_W + hc] = tile;
      grid[(hr + 1) * HALF_W + hc + 1] = tile;
    }
  }
  return grid;
}

/**
 * Generate enemy roster for a stage (20 enemies):
 * mix changes with stage difficulty. Light early; more Fast/Armor/Power later.
 *
 * 权重在生成前归一化到 ENEMY_QUOTA=20，保证 4 种类型在高难度关卡都不会被截断
 * （之前的 bug：stage 30+ 时 light+fast+armor 之和 ≥20，power 全被砍掉）。
 */
export function rosterFor(stageIdx: number): Array<
  "light" | "fast" | "armor" | "power"
> {
  const tier = Math.min(34, Math.max(0, stageIdx - 1));
  const rawWeights = {
    light: 18 - tier * 0.35,
    fast: 1 + tier * 0.18,
    armor: 0 + tier * 0.22,
    power: 1 + tier * 0.15,
  };
  const total = 20;
  const seq: Array<"light" | "fast" | "armor" | "power"> = [
    "light",
    "fast",
    "armor",
    "power",
  ];
  const sum = Math.max(
    0.0001,
    seq.reduce((acc, k) => acc + Math.max(0, (rawWeights as any)[k]), 0),
  );
  // 按比例分配；先 floor，余数按"小数部分由大到小"补 1，直到刚好 total
  const counts: Record<string, number> = {};
  const fracs: Array<{ k: string; frac: number }> = [];
  let used = 0;
  for (const k of seq) {
    const w = Math.max(0, (rawWeights as any)[k] as number);
    const exact = (w / sum) * total;
    const n = Math.floor(exact);
    counts[k] = n;
    used += n;
    fracs.push({ k, frac: exact - n });
  }
  fracs.sort((a, b) => b.frac - a.frac);
  let i = 0;
  while (used < total) {
    counts[fracs[i % fracs.length]!.k] = (counts[fracs[i % fracs.length]!.k] ?? 0) + 1;
    used++;
    i++;
  }
  const list: Array<"light" | "fast" | "armor" | "power"> = [];
  for (const k of seq) {
    const n = counts[k] ?? 0;
    for (let j = 0; j < n; j++) list.push(k);
  }
  // 截断容错（理论上 used === total，不需要）
  if (list.length > total) list.length = total;
  // shuffle deterministically by stage idx
  let s = stageIdx * 2654435761;
  for (let p = list.length - 1; p > 0; p--) {
    s = (s + 0x6d2b79f5) >>> 0;
    let t = s;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    const r = ((t ^ (t >>> 14)) >>> 0) % (p + 1);
    const tmp = list[p] as any;
    list[p] = list[r] as any;
    list[r] = tmp;
  }
  return list;
}
